using System;

namespace Triangle_Of_Number
{
    internal class Program
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Enter the Number");
            for(int i=1;i<=10;i++)
            {
                Console.WriteLine(i);
            }
            Console.WriteLine("The reverse Number is");
            for(int j=10;j>=1;j--)
            {
                Console.WriteLine(j);
            }
        }
    }
}
